<?php
session_start();
$total_money = 0; // Khởi tạo biến trước khi sử dụng
include 'config/config.php'; // Kết nối database

if ($_SERVER["REQUEST_METHOD"] == "POST") {
    $fullname = $_POST['fullname'];
    $email = $_POST['email'];
    $phone = $_POST['phone'];
    $address = $_POST['address'];
    $total_money = 0;

    // Tính tổng tiền từ giỏ hàng
    foreach ($_SESSION['cart'] as $item) {
        $total_money += $item['product_price'] * $item['quantity'];
    }

    // Lưu vào bảng orders
    $stmt = $conn->prepare("INSERT INTO orders (user_id, fullname, email, phone_number, address, order_date, total_money, status) VALUES (?, ?, ?, ?, ?, NOW(), ?, 1)");
    $stmt->bind_param("issssi", $_SESSION['user_id'], $fullname, $email, $phone, $address, $total_money);
    $stmt->execute();
    $order_id = $stmt->insert_id;

    // Lưu vào bảng order_detail
    foreach ($_SESSION['cart'] as $item) {
        $stmt = $conn->prepare("INSERT INTO order_detail (order_id, product_id, price, num, total_money) VALUES (?, ?, ?, ?, ?)");
        $subtotal = $item['product_price'] * $item['quantity'];
        $stmt->bind_param("iiiii", $order_id, $item['product_id'], $item['product_price'], $item['quantity'], $subtotal);
        $stmt->execute();
    }

    // Xóa giỏ hàng
    unset($_SESSION['cart']);

    echo "Đặt hàng thành công! Đơn hàng của bạn đã được lưu.";
